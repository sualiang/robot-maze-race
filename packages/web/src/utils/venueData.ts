/** 获取指定城市的区域选项（含过滤功能） */
export function getDistrictOptions(city: string): { value: string; label: string }[] {
  const districts = DISTRICT_MAP[city];
  if (!districts) return [];
  return districts.map((d) => ({ value: d, label: d }));
}
